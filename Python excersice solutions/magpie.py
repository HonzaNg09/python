a = int(input('Please enter an integer between 1 and 7: '))
if a == 1:
    print('One for sorrow')
elif a == 2: 
    print('Two for joy')
elif a == 3:
    print('Three for a girl')
elif a == 4: 
    print('Four for a boy')
elif a == 5:
    print('Five for silver')
elif a == 6:
    print('Six for gold')
elif a == 7: 
    print('Seven for a secret never to be told ')
else: 
    print('Not a permitted number')
    